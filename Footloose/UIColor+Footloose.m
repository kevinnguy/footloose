#import "UIColor+Footloose.h"

@implementation UIColor (Footloose)

+ (UIColor *)footloosePrimaryColor
{
    return [UIColor colorWithRed:0.357 green:0.149 blue:0.47 alpha:1];
}

+ (UIColor *)footlooseLightPrimaryColor
{
    return [UIColor colorWithRed:0.554 green:0.28 blue:0.704 alpha:0.939];
}

+ (UIColor *)defaultNavigationBarColor
{
    return [UIColor colorWithRed:(247/255.0) green:(247/255.0) blue:(247/255.0) alpha:1];
}

@end
